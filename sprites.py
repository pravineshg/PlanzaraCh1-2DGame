# ── grid constants ────────────────────────────────────────────────────
CW, CH = 12, 30


def draw_char(surf, art, ox, oy, bs=4):
    """Render character blocks onto surf at pixel (ox, oy) with block size bs."""
    for entry in art:
        if len(entry) == 5:
            bx, by, bw, bh, col = entry
            pygame.draw.rect(surf, col,
                             (ox + bx * bs, oy + by * bs, bw * bs, bh * bs))


def char_surf(art, bs=4, flip=False):
    """Return SRCALPHA Surface with character drawn on it."""
    w = (CW + 2) * bs + 2
    h = (CH + 2) * bs + 2
    s = pygame.Surface((w, h), pygame.SRCALPHA)
    draw_char(s, art, bs, bs, bs)
    return pygame.transform.flip(s, True, False) if flip else s